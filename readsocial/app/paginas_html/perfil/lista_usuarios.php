La transición política que atraviesa Bolivia exige un profundo rediseño institucional y el fortalecimiento de estructuras de democracia representativas articuladas con la instauración de nuevos mecanismos de participación e inclusión en un ámbito de pluralismo jurídico e institucional.

 

 

A nivel social, varios indicadores sociales siguen muy por debajo de los promedios regionales y persisten marcadas desigualdades de bienestar entre zonas rurales y urbanas, entre regiones, entre hombres y mujeres, y entre ciudadanos indígenas y no indígenas. Este panorama está relacionado con la existencia de importantes inequidades en el acceso y la calidad de servicios sociales básicos. El Gobierno se ha planteado el reto de avanzar hacia sistemas universales de salud y educación en el marco de la “Agenda Patriótica del Segundo Centenario” propuesta por el Presidente del Estado Plurinacional de Bolivia en agosto de 2012.

 

 Bolivia también enfrenta desafíos de sostenibilidad ambiental derivados de la reducción de su patrimonio natural y su creciente vulnerabilidad frente a los efectos del cambio climático y los desastres naturales. La pérdida acelerada de la diversidad biológica y la degradación de los recursos hídricos y forestales (se estiman unas 350 mil hectáreas deforestadas por año), así como la contaminación ambiental de los centros poblados, son factores que comprometen el alcance de los ODM relacionados a la sostenibilidad de un modelo de desarrollo organizado en torno a la explotación de los recursos naturales y la ulterior generación de pasivos ambientales y sociales.

Logros
 

La situación socioeconómica ha mejorado significativamente, la incidencia de la pobreza extrema se redujo de 38,2% en 2005 a 25,4% en 2010; en el mismo periodo la pobreza moderada se redujo de 60,6% a 49,6% (UDAPE).

 

Se estima que el país tiene una alta posibilidad de alcanzar las metas relativas a reducción de pobreza, desnutrición, malaria y cobertura de agua potable, mientras que se alcanzarán parcialmente las de educación e igualdad de género, quedando como objetivos pendientes los relativos a salud materno-infantil, tuberculosis, VIH-SIDA y Chagas.</p>
